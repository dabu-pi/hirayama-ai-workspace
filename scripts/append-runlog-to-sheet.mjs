#!/usr/bin/env node

import { createSign } from 'node:crypto';
import { existsSync, readFileSync } from 'node:fs';
import { basename, resolve } from 'node:path';

const DEFAULT_SCOPE = 'https://www.googleapis.com/auth/spreadsheets';
const DEFAULT_SHEET_NAME = 'Run_Log';
const DEFAULT_ENV_ACCOUNT = 'AIOS_SERVICE_ACCOUNT_PATH';
const DEFAULT_ENV_SPREADSHEET = 'AIOS_DASHBOARD_SPREADSHEET_ID';
const DEFAULT_ENV_SHEET = 'AIOS_RUNLOG_SHEET_NAME';
const DEFAULT_ENV_WRITE = 'AIOS_RUNLOG_SHEET_WRITE';

function parseArgs(argv) {
  const args = {};
  for (let i = 0; i < argv.length; i += 1) {
    const token = argv[i];
    if (!token.startsWith('--')) {
      continue;
    }
    const key = token.slice(2);
    const next = argv[i + 1];
    if (!next || next.startsWith('--')) {
      args[key] = 'true';
      continue;
    }
    args[key] = next;
    i += 1;
  }
  return args;
}

function printHelp() {
  console.log(`append-runlog-to-sheet.mjs

Usage:
  node scripts/append-runlog-to-sheet.mjs --json <path> [--write]

Options:
  --json <path>           Path to the Run_Log JSON generated by export-run-log-entry.ps1
  --spreadsheet-id <id>   Override ${DEFAULT_ENV_SPREADSHEET}
  --sheet-name <name>     Override ${DEFAULT_ENV_SHEET} (default: ${DEFAULT_SHEET_NAME})
  --service-account <p>   Override ${DEFAULT_ENV_ACCOUNT}
  --write                 Actually write to Google Sheets
  --dry-run               Print the row and resolved config only
  --help                  Show this help

Environment:
  ${DEFAULT_ENV_ACCOUNT}       Absolute or relative path to service_account.json
  ${DEFAULT_ENV_SPREADSHEET}   Spreadsheet ID for Hirayama AI OS Dashboard
  ${DEFAULT_ENV_SHEET}         Worksheet name (optional)
  ${DEFAULT_ENV_WRITE}=1       Allow writes without passing --write
`);
}

function base64UrlEncode(value) {
  return Buffer.from(value)
    .toString('base64')
    .replace(/\+/g, '-')
    .replace(/\//g, '_')
    .replace(/=+$/g, '');
}

function loadJson(filePath) {
  const fullPath = resolve(filePath);
  if (!existsSync(fullPath)) {
    throw new Error(`JSON file not found: ${fullPath}`);
  }
  const raw = readFileSync(fullPath, 'utf8').replace(/^\uFEFF/, '');
  return JSON.parse(raw);
}

function loadServiceAccount(filePath) {
  const fullPath = resolve(filePath);
  if (!existsSync(fullPath)) {
    throw new Error(`service_account.json not found: ${fullPath}`);
  }
  const account = JSON.parse(readFileSync(fullPath, 'utf8'));
  if (!account.client_email || !account.private_key || !account.token_uri) {
    throw new Error(`Invalid service account file: ${fullPath}`);
  }
  return { account, fullPath };
}

function createJwt({ clientEmail, privateKey, tokenUri, scope }) {
  const now = Math.floor(Date.now() / 1000);
  const header = { alg: 'RS256', typ: 'JWT' };
  const payload = {
    iss: clientEmail,
    scope,
    aud: tokenUri,
    exp: now + 3600,
    iat: now,
  };

  const encodedHeader = base64UrlEncode(JSON.stringify(header));
  const encodedPayload = base64UrlEncode(JSON.stringify(payload));
  const signingInput = `${encodedHeader}.${encodedPayload}`;

  const signer = createSign('RSA-SHA256');
  signer.update(signingInput);
  signer.end();
  const signature = signer.sign(privateKey, 'base64')
    .replace(/\+/g, '-')
    .replace(/\//g, '_')
    .replace(/=+$/g, '');

  return `${signingInput}.${signature}`;
}

async function fetchAccessToken(account) {
  const assertion = createJwt({
    clientEmail: account.client_email,
    privateKey: account.private_key,
    tokenUri: account.token_uri,
    scope: DEFAULT_SCOPE,
  });

  const body = new URLSearchParams({
    grant_type: 'urn:ietf:params:oauth:grant-type:jwt-bearer',
    assertion,
  });

  const response = await fetch(account.token_uri, {
    method: 'POST',
    headers: {
      'Content-Type': 'application/x-www-form-urlencoded',
    },
    body,
  });

  if (!response.ok) {
    const text = await response.text();
    throw new Error(`Failed to obtain access token (${response.status}): ${text}`);
  }

  const payload = await response.json();
  if (!payload.access_token) {
    throw new Error('Access token response did not include access_token');
  }
  return payload.access_token;
}

function toRow(entry) {
  return [
    entry.log_id ?? '',
    entry.datetime ?? '',
    entry.system ?? '',
    entry.project ?? '',
    entry.summary ?? '',
    entry.result ?? '',
    entry.commit_hash ?? '',
    entry.tasks_done ?? '',
    entry.stop_reason ?? '',
    entry.next_action ?? '',
  ];
}

async function appendRow({ spreadsheetId, sheetName, row, accessToken }) {
  const range = encodeURIComponent(`${sheetName}!A:J`);
  const url = `https://sheets.googleapis.com/v4/spreadsheets/${spreadsheetId}/values/${range}:append?valueInputOption=USER_ENTERED&insertDataOption=INSERT_ROWS`;
  const response = await fetch(url, {
    method: 'POST',
    headers: {
      Authorization: `Bearer ${accessToken}`,
      'Content-Type': 'application/json',
    },
    body: JSON.stringify({
      majorDimension: 'ROWS',
      values: [row],
    }),
  });

  if (!response.ok) {
    const text = await response.text();
    throw new Error(`Failed to append row (${response.status}): ${text}`);
  }

  return response.json();
}

async function main() {
  const args = parseArgs(process.argv.slice(2));
  if (args.help === 'true') {
    printHelp();
    return;
  }

  const jsonPath = args.json;
  if (!jsonPath) {
    throw new Error('Missing required --json argument');
  }

  const spreadsheetId = args['spreadsheet-id'] || process.env[DEFAULT_ENV_SPREADSHEET] || '';
  const sheetName = args['sheet-name'] || process.env[DEFAULT_ENV_SHEET] || DEFAULT_SHEET_NAME;
  const serviceAccountPath = args['service-account'] || process.env[DEFAULT_ENV_ACCOUNT] || '';
  const shouldWrite = args.write === 'true' || process.env[DEFAULT_ENV_WRITE] === '1';
  const isDryRun = args['dry-run'] === 'true' || !shouldWrite;

  const entry = loadJson(jsonPath);
  const row = toRow(entry);

  console.log(`[INFO] Run_Log JSON : ${resolve(jsonPath)}`);
  console.log(`[INFO] Spreadsheet  : ${spreadsheetId || '(unset)'}`);
  console.log(`[INFO] Sheet name   : ${sheetName}`);
  console.log(`[INFO] Row values   : ${JSON.stringify(row)}`);

  if (isDryRun) {
    console.log('[INFO] Dry run mode. No data was written to Google Sheets.');
    return;
  }

  if (!spreadsheetId) {
    throw new Error(`Spreadsheet ID is missing. Set ${DEFAULT_ENV_SPREADSHEET} or pass --spreadsheet-id.`);
  }
  if (!serviceAccountPath) {
    throw new Error(`Service account path is missing. Set ${DEFAULT_ENV_ACCOUNT} or pass --service-account.`);
  }

  const { account, fullPath } = loadServiceAccount(serviceAccountPath);
  console.log(`[INFO] Service acct : ${basename(fullPath)}`);

  const accessToken = await fetchAccessToken(account);
  const result = await appendRow({
    spreadsheetId,
    sheetName,
    row,
    accessToken,
  });

  const updatedRange = result?.updates?.updatedRange ?? '(unknown)';
  console.log(`[OK] Appended Run_Log row to ${updatedRange}`);
}

main().catch((error) => {
  console.error(`[ERR] ${error.message}`);
  process.exit(1);
});
